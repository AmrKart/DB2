    private void ComboBoxTableNameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboBoxTableNameItemStateChanged
        // TODO add your handling code here:               
        if(evt.getStateChange() == ItemEvent.SELECTED){
            String selectedTable = ComboBoxTableName.getSelectedItem().toString();                        
            if(selectedTable.equals("Select Table To View"))
            {         
                columnsPanel.removeAll(); // Clear the components from the panel
                columnsPanel.revalidate();
                columnsPanel.repaint();
                return;
            }         
            
             columnsPanel.removeAll(); // Clear the components from the panel
             columnsPanel.setLayout(new GridLayout());

             
             ArrayList<String> columnsName = DataBaseHelper.getColumnsNames(selectedTable);
             for(int i=1;i<columnsName.size();i++)
             {
                 JTextField colTextField = new JTextField(columnsName.get(i));
                 colTextField.setBackground(Color.GRAY);
                 colTextField.setForeground(Color.BLACK);
                 columnsPanel.add(colTextField);
             }

             columnsPanel.revalidate();
             columnsPanel.repaint();
        }        
    }//GEN-LAST:event_ComboBoxTableNameItemStateChanged

