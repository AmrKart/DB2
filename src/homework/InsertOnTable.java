    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        // TODO add your handling code here:
        String selectedTable = ComboBoxTableName.getSelectedItem().toString();                        
        ArrayList<String> row= new ArrayList<String>();
                
        for (Component c : columnsPanel.getComponents()) {
            if (c instanceof JTextField) {
                row.add(((JTextField) c).getText());                
            }
        }
        
        if(DataBaseHelper.validateInput(selectedTable, row)){
            DataBaseHelper.insert(selectedTable, row);
        }
    }//GEN-LAST:event_insertBtnActionPerformed

    private void resetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetBtnActionPerformed
        // TODO add your handling code here:
        
        String selectedTable = ComboBoxTableName.getSelectedItem().toString();                        
        
        ArrayList<String> columnsName = DataBaseHelper.getColumnsNames(selectedTable);
        int index=1;
        for (Component c : columnsPanel.getComponents()) {
            if (c instanceof JTextField) {
                ((JTextField) c).setText(columnsName.get(index));
                index++;
            }
        }
    }//GEN-LAST:event_resetBtnActionPerformed

